function SWEP:PrimaryAttack()
if self:Ammo1() > 0 then
	self.Owner:SetAnimation(PLAYER_ATTACK1)
	self.Idle = CurTime() + self.Owner:GetViewModel():SequenceDuration()
	local tr = {}
	tr.start = self.Owner:GetShootPos()
	tr.endpos = self.Owner:GetShootPos() + ( self.Owner:GetAimVector() * 200 )
	tr.filter = self.Owner
	tr.mask = MASK_SHOT
	local trace = util.TraceLine( tr )
	if trace.Hit and trace.HitPos:Distance(self.Owner:GetShootPos()) <= 200 then
	self.Weapon:SetNextPrimaryFire(CurTime() + self.Primary.Delay)
	self.Weapon:SetNextSecondaryFire(CurTime() + self.Primary.Delay)
	self:TakePrimaryAmmo(1)
	if SERVER then
	local sk = ents.Create("tf2custom_proj_skeleton")
	sk:SetPos(trace.HitPos)
	sk:SetAngles(Angle(0,self.Owner:EyeAngles().y,0))
	sk:SetOwner(self.Owner)
	sk:Spawn()
	end
	else
	self:EmitSound("weapons/medigun_no_target.wav")
	self.Weapon:SetNextPrimaryFire(CurTime() + .2)
	self.Weapon:SetNextSecondaryFire(CurTime() + .2)
end
	else
	self:EmitSound("weapons/medigun_no_target.wav")
	self.Weapon:SetNextPrimaryFire(CurTime() + .2)
	self.Weapon:SetNextSecondaryFire(CurTime() + .2)
end
end


