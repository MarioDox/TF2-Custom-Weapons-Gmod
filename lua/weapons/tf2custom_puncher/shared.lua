function SWEP:Melee()
self.ChargeStart = CurTime() + 1
	self.DoMelee = nil
	local tr = {}
	tr.start = self.Owner:GetShootPos()
	tr.endpos = self.Owner:GetShootPos() + ( self.Owner:GetAimVector() * 80 )
	tr.filter = self.Owner
	tr.mask = MASK_SHOT
	local trace = util.TraceLine( tr )

	if ( trace.Hit ) then
			bullet = {}
			bullet.Num    = 1
			bullet.Src    = self.Owner:GetShootPos()
			bullet.Dir    = self.Owner:GetAimVector()
			bullet.Spread = Vector(0, 0, 0)
			bullet.Tracer = 0
			bullet.Force  = 1
			if self.Weapon:GetNWBool("Critical") then
			bullet.Damage = 52.5
			trace.Entity:EmitSound("TFPlayer.CritHit")
			else
			bullet.Damage = 17.5
			end
			if trace.HitGroup == HITGROUP_HEAD and (trace.Entity:IsPlayer() or trace.Entity:IsNPC()) then
			bullet.Src = trace.Entity:GetBonePosition(trace.Entity:LookupBone("ValveBiped.Bip01_Spine2"))
			trace.Entity:TF2Stun(5,nil,"mvm_pow_bam",80)
			end
			if trace.Entity:IsPlayer() or trace.Entity:IsNPC() or trace.Entity:IsRagdoll() then
			self.Weapon:EmitSound("Weapon_BoxingGloves.HitFlesh")
			else
			self.Weapon:EmitSound("Weapon_BoxingGloves.HitWorld")
			end
			self.Owner:FireBullets(bullet) 
	end
			self.Weapon:SetNWBool("Critical", false)
end

